respuesta1 = int(input("Quiero que me pases 1 numero enteron\n"))

respuesta2 = int(input("Ahora quiero otro numero mas porfa\n"))

print(f"El resultado de la suma de {respuesta1} y {respuesta2} es: {round(respuesta1 + respuesta2)}")
print(f"El resultado de la resta de {respuesta1} y {respuesta2} es: {round(respuesta1 - respuesta2)}")
print(f"El resultado de la multiplicacion de {respuesta1} y {respuesta2} es: {round(respuesta1 * respuesta2)}")
print(f"El resultado de la division de {respuesta1} y {respuesta2} es: {round(respuesta1 / respuesta2)}")
print(f"El resto de la division de {respuesta1} y {respuesta2} es: {round(respuesta1 % respuesta2)}")


